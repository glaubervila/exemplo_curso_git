def multiplica(a, b):
    """Retorna o resultado da multiplicação de dois números

    Args:
        a (int): número inteiro
        b (int): número inteiro

    Returns:
        int: Resultado da multiplicação de "a" e "b"
    """
    return int(a) * int(b)


