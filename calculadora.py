def soma(a, b):
    """Retorna a soma entre 2 valores inteiros

    Args:
        a (int): qualquer valor inteiro
        b (int): qualquer valor inteiro

    Returns:
        int: Resultado da soma entre a e b
    """
    return int(a) + int(b)


