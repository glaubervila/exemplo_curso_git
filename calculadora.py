def subtracao(a, b):
    """Retorna a subtração entre 2 valores inteiros
    Args:
        a (int): qualquer valor inteiro
        b (int): qualquer valor inteiro
    Returns:
        int: Resultado da subtração entre a e b
    """
    return int(a) - int(b)


